        })
        .catch(error => {
            console.log('Error fetching online visitors');
        });
}

// Update pertama kali
document.addEventListener('DOMContentLoaded', function() {
    updateOnlineVisitorsBackend();
    setInterval(updateOnlineVisitorsBackend, 30000);
});
